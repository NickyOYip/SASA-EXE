/**
 * Utility functions for working with category data
 */
const CategoryDropdownUtils = {
    /**
     * Get all main categories
     * @returns {Array} Array of main categories
     */
    getMainCategories: function() {
        return categoryDropdownData.mainCategories;
    },

    /**
     * Get subcategories for a specific category
     * @param {string} categoryKey - The key for the category (e.g., 'skincare')
     * @returns {Array} Array of subcategories
     */
    getSubcategories: function(categoryKey) {
        return categoryDropdownData.subcategories[categoryKey] || [];
    },

    /**
     * Get product categories
     * @param {string} categoryKey - The key for the product category
     * @returns {Array} Array of product categories
     */
    getProductCategories: function(categoryKey) {
        return categoryDropdownData.productCategories[categoryKey] || [];
    },

    /**
     * Get all recommended brands
     * @returns {Array} Array of recommended brands
     */
    getRecommendedBrands: function() {
        return categoryDropdownData.recommendedBrands;
    },

    /**
     * Find category by ID
     * @param {number} categoryId - The category ID to search for
     * @returns {Object|null} The category object or null if not found
     */
    findCategoryById: function(categoryId) {
        // Search in main categories
        const mainCategory = this.getMainCategories().find(cat => cat.id === categoryId);
        if (mainCategory) return mainCategory;

        // Search in subcategories
        for (const key in categoryDropdownData.subcategories) {
            const subcategory = categoryDropdownData.subcategories[key].find(cat => cat.id === categoryId);
            if (subcategory) return subcategory;
        }

        // Search in product categories
        for (const key in categoryDropdownData.productCategories) {
            const productCategory = categoryDropdownData.productCategories[key].find(cat => cat.id === categoryId);
            if (productCategory) return productCategory;
        }

        return null;
    },

    /**
     * Get active category
     * @returns {Object|null} The active category object
     */
    getActiveCategory: function() {
        return this.getMainCategories().find(cat => cat.isActive) || null;
    },

    /**
     * Get active subcategory
     * @param {string} categoryKey - The key for the category
     * @returns {Object|null} The active subcategory object
     */
    getActiveSubcategory: function(categoryKey) {
        const subcategories = this.getSubcategories(categoryKey);
        return subcategories.find(cat => cat.isActive) || null;
    }
};

